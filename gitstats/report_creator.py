    def create_hotspots_html(self, data: Any, path: str) -> None:
        """
        Create a Hotspots Analysis page with scatter plot of change frequency × file size.

        Hotspots are files that are both frequently changed and large in size —
        they are the most bug-prone files in the codebase (CodeScene concept).
        """
        hotspot_data = getattr(data, "hotspot_files", {})
        if not hotspot_data:
            return

        f = open(path + "/hotspots.html", "w", encoding="utf-8")
        self.print_header(f)
        self.print_nav(f)
        f.write("<h1>Hotspots Analysis</h1>")

        f.write("""
        <div class="hotspots-intro">
            <p><strong>Hotspots</strong> are files that combine high change frequency with large size.
            These are the most bug-prone files in your codebase — they change often
            <em>and</em> have many lines of code, making each change risky.</p>
            <p><strong>Quadrant guide:</strong></p>
            <ul>
                <li><span class="hotspot-dot hotspot-critical">⬤</span> <strong>Critical (top-right):</strong>
                High churn + large size — refactoring candidates</li>
                <li><span class="hotspot-dot hotspot-warning">⬤</span> <strong>Warning (top-left):</strong>
                High churn but small — frequent changes, lower risk</li>
                <li><span class="hotspot-dot hotspot-calm">⬤</span> <strong>Stable (bottom-right):</strong>
                Large but rarely changed — mature code</li>
                <li><span class="hotspot-dot hotspot-safe">⬤</span> <strong>Safe (bottom-left):</strong>
                Small and rarely changed</li>
            </ul>
            <p>Dashed lines show the median values for file size and change frequency.</p>
        </div>
        """)

        # Build scatter data: filter to files with churn > 0 and lines > 0
        scatter_points = []
        for filepath, info in hotspot_data.items():
            churn = info.get("churn", 0)
            lines = info.get("lines", 0)
            score = info.get("score", 0.0)
            if churn > 0 and lines > 0:
                scatter_points.append(
                    {
                        "path": filepath,
                        "x": lines,
                        "y": churn,
                        "score": round(score, 1),
                    }
                )

        if not scatter_points:
            f.write(
                "<p>No hotspot data available. Files must have been touched by at least one commit and have > 0 lines.</p>"
            )
            self.print_footer(f)
            f.write("</body></html>")
            f.close()
            return

        # Compute medians for quadrant lines
        lines_list = [p["x"] for p in scatter_points]
        churn_list = [p["y"] for p in scatter_points]
        lines_list.sort()
        churn_list.sort()
        n = len(scatter_points)
        median_x = lines_list[n // 2] if n else 0
        median_y = churn_list[n // 2] if n else 0

        # Serialize to JSON for Chart.js
        scatter_json = json.dumps(scatter_points).replace("</", "<\\/")

        f.write(f"""
        <div class="hotspots-summary">
            <p>Showing {len(scatter_points)} files with at least one change.</p>
        </div>
        <div style="max-width:100%;margin-bottom:8px">
            <canvas id="chart-hotspots"></canvas>
        </div>
        <script>
        (function() {{
            var points = {scatter_json};

            var medianX = {median_x};
            var medianY = {median_y};

            // Build Chart.js scatter datasets with color by quadrant
            // Point size scales with hotspot score: bigger = riskier
            var allScores = points.map(function(p) {{ return p.score; }});
            var maxScore = Math.max.apply(null, allScores);
            var minScore = Math.min.apply(null, allScores);
            var scoreRange = maxScore - minScore || 1;

            var scatterData = points.map(function(p) {{
                var color;
                var quadrant;
                if (p.x >= medianX && p.y >= medianY) {{
                    color = '#cf222e';  // critical (top-right)
                    quadrant = 'critical';
                }} else if (p.x < medianX && p.y >= medianY) {{
                    color = '#e16f24';  // warning (top-left)
                    quadrant = 'warning';
                }} else if (p.x >= medianX && p.y < medianY) {{
                    color = '#1a7f37';  // calm (bottom-right)
                    quadrant = 'calm';
                }} else {{
                    color = '#5b8dee';  // safe (bottom-left)
                    quadrant = 'safe';
                }}
                // Normalize radius: 3 (min) to 12 (max) based on score percentile
                var norm = (p.score - minScore) / scoreRange;
                var radius = 3 + norm * 9;
                return {{
                    x: p.x,
                    y: p.y,
                    path: p.path,
                    score: p.score,
                    quadrant: quadrant,
                    backgroundColor: color,
                    borderColor: color,
                    pointRadius: radius,
                    pointHoverRadius: radius + 4,
                }};
            }});

            // Custom plugin to draw quadrant median lines
            var quadrantPlugin = {{
                id: 'quadrantLines',
                afterDraw: function(chart) {{
                    var ctx = chart.ctx;
                    var chartArea = chart.chartArea;
                    var xScale = chart.scales.x;
                    var yScale = chart.scales.y;

                    var x = xScale.getPixelForValue(medianX);
                    var y = yScale.getPixelForValue(medianY);

                    ctx.save();
                    ctx.strokeStyle = 'rgba(100, 100, 100, 0.6)';
                    ctx.lineWidth = 1.5;
                    ctx.setLineDash([6, 4]);

                    // Vertical line (median file size)
                    ctx.beginPath();
                    ctx.moveTo(x, chartArea.top);
                    ctx.lineTo(x, chartArea.bottom);
                    ctx.stroke();

                    // Horizontal line (median change frequency)
                    ctx.beginPath();
                    ctx.moveTo(chartArea.left, y);
                    ctx.lineTo(chartArea.right, y);
                    ctx.stroke();

                    ctx.restore();
                }}
            }};

            var ctx = document.getElementById('chart-hotspots').getContext('2d');
            new Chart(ctx, {{
                type: 'scatter',
                data: {{
                    datasets: [{{
                        label: 'Files',
                        data: scatterData,
                        pointHitRadius: 10,
                    }}]
                }},
                options: {{
                    responsive: true,
                    maintainAspectRatio: true,
                    aspectRatio: 1.6,
                    plugins: {{
                        legend: {{ display: false }},
                        tooltip: {{
                            callbacks: {{
                                label: function(context) {{
                                    var p = context.raw;
                                    return ' ' + p.path + ' | ' + p.x + ' lines, changed ' + p.y + ' times, score: ' + p.score;
                                }}
                            }}
                        }}
                    }},
                    scales: {{
                        x: {{
                            type: 'logarithmic',
                            title: {{ display: true, text: 'File Size (lines of code)' }},
                            min: 1,
                            ticks: {{
                                callback: function(value) {{
                                    if (value >= 1000) return (value/1000) + 'k';
                                    return value;
                                }}
                            }}
                        }},
                        y: {{
                            type: 'logarithmic',
                            title: {{ display: true, text: 'Change Frequency (commits)' }},
                            min: 1,
                            ticks: {{
                                callback: function(value) {{
                                    return value;
                                }}
                            }}
                        }}
                    }}
                }},
                plugins: [quadrantPlugin]
            }});
        }})();
        </script>
        """)

        # Table of top hotspot files
        f.write(html_header(2, "Top Hotspot Files"))
        f.write(
            "<p><em>Sorted by hotspot score (churn × √lines). Higher score = higher risk.</em></p>"
        )

        # Sort by score descending, take top 50
        scatter_points.sort(key=lambda p: p["score"], reverse=True)
        top_hotspots = scatter_points[:50]

        # Use quartile-based thresholds for risk levels (data-adaptive)
        sorted_scores = sorted(p["score"] for p in top_hotspots)
        n_scores = len(sorted_scores)
        if n_scores >= 4:
            threshold_warning = sorted_scores[n_scores // 4]  # 25th percentile
            threshold_critical = sorted_scores[3 * n_scores // 4]  # 75th percentile
        else:
            threshold_warning = 0
            threshold_critical = sorted_scores[-1] if sorted_scores else 0

        f.write("""
        <table class="sortable" id="hotspots">
        <tr>
            <th>File</th>
            <th>Lines of Code</th>
            <th>Times Changed</th>
            <th>Hotspot Score</th>
            <th>Risk Level</th>
        </tr>
        """)

        for p in top_hotspots:
            score = p["score"]
            if score >= threshold_critical:
                risk = "Critical"
                risk_class = "risk-critical"
            elif score >= threshold_warning:
                risk = "Warning"
                risk_class = "risk-warning"
            else:
                risk = "Monitor"
                risk_class = "risk-monitor"
            f.write(
                '<tr><td>%s</td><td>%d</td><td>%d</td><td>%.1f</td><td class="%s">%s</td></tr>'
                % (html.escape(p["path"]), p["x"], p["y"], score, risk_class, risk)
            )

        f.write("</table>")

        # Stats summary
        f.write(html_header(2, "Summary Statistics"))
        total_hotspot_candidates = sum(
            1 for p in scatter_points if p["x"] >= median_x and p["y"] >= median_y
        )
        f.write("<dl>")
        f.write("<dt>Files analyzed</dt><dd>%d</dd>" % len(scatter_points))
        f.write("<dt>Median file size</dt><dd>%d lines</dd>" % median_x)
        f.write("<dt>Median change frequency</dt><dd>%d commits</dd>" % median_y)
        f.write(
            "<dt>Critical hotspots (top-right quadrant)</dt><dd>%d</dd>" % total_hotspot_candidates
        )
        f.write(
            "<dt>Total lines in hotspots</dt><dd>%d</dd>"
            % sum(p["x"] for p in scatter_points if p["x"] >= median_x and p["y"] >= median_y)
        )
        f.write("</dl>")

        self.print_footer(f)
        f.write("</body></html>")
        f.close()

